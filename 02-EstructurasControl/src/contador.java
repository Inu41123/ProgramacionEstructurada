public class contador {

    public static void main(String[] args) {
        int n = 1;
        int i = 5;

        while (i >= n){

            System.out.println(i);
            i--;

        }

        System.out.println("  ");

        for (int s = 5; s >= n; s--){

            System.out.println(s);

        }

    }
}
